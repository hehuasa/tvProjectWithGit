export const getTreeData = (req, res) => {
  const { parentTreeID } = req.body;
  const cacheData = [];
  if (!parentTreeID) {
    for (const data of treeData) {
      if (data.parentTreeID === null) {
        cacheData.push(data);
      }
    }
  } else {
    for (const data of treeData) {
      if (Number(parentTreeID) === data.parentTreeID) {
        cacheData.push(data);
      }
    }
  }
  res.send({ code: 1001, data: cacheData });
};
